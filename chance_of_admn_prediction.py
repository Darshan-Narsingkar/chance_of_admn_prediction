# step 1: import library
import pandas as pd

# step 2: import data
admission = pd.read_csv('https://github.com/YBI-Foundation/Dataset/raw/main/Admission%20Chance.csv')

admission.head()

admission.info()

admission.describe()

# step 3: Describe targer(y) and features (x)
admission.columns

y = admission ['Chance of Admit ']

x = admission[['Serial No', 'GRE Score', 'TOEFL Score', 'University Rating', ' SOP',
       'LOR ', 'CGPA', 'Research']]

# step 4: train_test_split
from sklearn.model_selection import train_test_split
x_train, x_test, y_train, y_test = train_test_split(x,y, train_size=0.7 ,random_state=2529)

# check shape of train and test sample
x_train.shape, x_test.shape, y_train.shape, y_test.shape

# step 5: select model
from sklearn.linear_model import LinearRegression
model = LinearRegression()

# step 6: train or fit model
model.fit(x_train, y_train)

model.intercept_

model.coef_

# step 7 : predict model
y_predict = model.predict(x_test)

y_predict

# step 8: model accuracy
from sklearn.metrics import mean_absolute_error, mean_absolute_percentage_error,mean_squared_error

mean_absolute_error(y_test,y_predict)

mean_absolute_percentage_error(y_test,y_predict)

mean_squared_error(y_test,y_predict)